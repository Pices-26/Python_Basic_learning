friends = ['kevin', 'bob', 'steve', 'tim', 'frank']
'''printing lists'''
print(friends)

'''printing a specific index'''
print(friends[1])

''' prints from the back'''
print(friends[-1])

'''prints from index 1 up to not including 3'''
print(friends[1:3])

'''changing in list'''
friends[1] = 'mike'
print(friends[1])


